    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        PreparedStatement pst,pst1 = null;
        ResultSet rs = null;

    
        int a=Integer.parseInt(jTextField4.getText());
        int b = Integer.parseInt(jTextField5.getText());
        Integer c = new Integer(a-b);
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/atm", "root","");
            pst = con.prepareStatement("UPDATE `atm` SET Balance=? WHERE AccountID=?");
            pst.setString(1,c.toString());
            pst.setString(2,jTextField1.getText());
            pst.executeUpdate();
            
            pst1 = con.prepareStatement("SELECT * FROM `atm` WHERE AccountID=?");
            pst1.setString(1, jTextField6.getText());
            rs = pst1.executeQuery();
            if(rs.next()){
            int m = Integer.parseInt(rs.getString("Balance"));
            Integer n = new Integer (m+b);
            pst.setString(1,n.toString());
            pst.setString(2,jTextField6.getText());
            pst.executeUpdate();
            
            
            JOptionPane.showMessageDialog(null, "Transfer Success");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

